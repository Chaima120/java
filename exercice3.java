public class exercice3 {
    public static void main(String[] args) {
        // Déclaration et initialisation du tableau de prénoms
        String[] prenoms = {"Alice", "Bob", "Charlie", "David", "Eve"};

        // Boucle for pour parcourir et afficher chaque prénom
        for (int i = 0; i < prenoms.length; i++) {
            // Affiche le prénom courant
            System.out.println(prenoms[i]);
        }
    }
}
